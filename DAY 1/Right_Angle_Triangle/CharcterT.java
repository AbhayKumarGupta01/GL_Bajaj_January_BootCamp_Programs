//package DAY 1.Right_Angle_Triangle;

public class CharcterT {
  public static void main(String[] args) {
    int i,j;
    int n=5; 
    //char c= 'A';
    for(i=0; i<n; i++){
        char c='A';
        for(j=0; j<=i; j++){
            System.out.print(c);
            c++;
        }
        System.out.println(" ");
    }
  }  
}
